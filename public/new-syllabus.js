// redeclaring a global variable
const syllabus = UPDATED_SYLLABUS

// get subject weightage based 
function getSubjectWeightage() {
    const result = {};
    Object.keys(syllabus).forEach(subjectName => {
      const subject = syllabus[subjectName];
      result[subjectName] = subject.questions;
    });
    return result;
  }

// get weightages of units 
function getUnitsWeightage() {
    const result = {}
    for (const subject in syllabus) {
        if (syllabus.hasOwnProperty(subject)) {
            const units = syllabus[subject].units;
            for (const unitKey in units) {
                if (units.hasOwnProperty(unitKey)) {
                    const unit = units[unitKey];
                    result[unitKey] = unit.questions;
                }
            }
        }
    }
    return result;
}

// get chapters by subject name
function getChaptersBySubject() {
    const result = {};
    Object.keys(syllabus).forEach(subjectName => {
        const subject = syllabus[subjectName];
        const chapters = [];
        Object.keys(subject.units).forEach(topicName => {
            chapters.push(...subject.units[topicName].chapters);
        });
        result[subjectName] = chapters;
    });
    return result;
}

// get units by subject name
function getUnitsBySubject() {
    const result = {};
    Object.keys(syllabus).forEach(subjectName => {
        const subject = syllabus[subjectName];
        const units = [];
        Object.keys(subject.units).forEach(topicName => {
            units.push(topicName);
        });
        result[subjectName] = units;
    });
    return result;
}

// get all chapters from the syllabus
function getAllChaptersArray() {
    const allChapters = [];
    Object.keys(syllabus).forEach(subjectName => {
        const subject = syllabus[subjectName];
        Object.keys(subject.units).forEach(topicName => {
            allChapters.push(...subject.units[topicName].chapters);
        });
    });
    return allChapters;
}

// get all units as a single array
function getAllUnitsArray() {
    const allUnits = [];
    Object.keys(syllabus).forEach(subjectName => {
      const subject = syllabus[subjectName];
      Object.keys(subject.units).forEach(unitName => {
        allUnits.push(unitName);
      });
    });
    return allUnits;
  }
  
  // get all chapters of a specific subject
function getAllChaptersOfSpecificSubject(targetSubject) {
    const result = [];
    if (syllabus[targetSubject]) {
      const subject = syllabus[targetSubject];
      Object.keys(subject.units).forEach(topicName => {
        result.push(...subject.units[topicName].chapters);
      });
    }
    return result;
  }
  
  // get all units of a specific subject
function getAllUnitsOfSpecificSubject(targetSubject) {
    const result = [];
    if (syllabus[targetSubject]) {
      const subject = syllabus[targetSubject];
      result.push(...Object.keys(subject.units));
    }
    return result;
  }
  
// get all chapters of a specific subject and unit
function getAllChaptersForSubjectAndUnit(targetSubject, targetUnit) {
    if (syllabus[targetSubject] && syllabus[targetSubject].units[targetUnit]) {
      return syllabus[targetSubject].units[targetUnit].chapters;
    } else {
      return null;
    }
  }

